/**
 * Task 1, Lab 1, Chapter 4. 
 * This program prints the words "I love Java" 10 times on separate lines. 
 */
public class LabOneCounter {

	public static void main(String[] args) {
		
		String love = "I love Java";
		
		int i = 1;
		
		while (i<=10) {
			System.out.println(love);
			i++;
		}

	}

}
